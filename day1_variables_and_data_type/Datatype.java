public class Datatype {
    public static void main(String args[]){
        char ch = 'a';
        System.out.println(ch);
        boolean bol = true;
        System.out.println(bol);
        float flo = 6.5f;
        System.out.println(flo);
        double dou = 6.454;
        System.out.print(dou);
    }   
}
