public class switches {
    public static void main(String args []){
        int number = 3;

        switch(number){
            case 1 : System.out.print("samosa");
                     break;
            case 2 : System.out.print("mango shake");
                     break;
            case 3 : System.out.print("milk shake");
                     break;
            default : System.out.print("wake up");
            


        }

    }
    
}
